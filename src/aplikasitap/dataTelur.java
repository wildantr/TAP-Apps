    //menampilkan data yang ada di database ke tabel
    public void tampildata() {
        Object[] row = {"ID", "Tanggal", "Telur /kg", "Sehat /butir", "Cacat /butir",
            "Kalkulasi butir"};
        model = new DefaultTableModel(null, row);
        jTable2.setModel(model);
        try {
            String sql = "SELECT * FROM dataTelur order by tgl asc";
            Statement stat = con.createStatement();
            ResultSet set = stat.executeQuery(sql);
            while (set.next()) {
                String ID = set.getString(1);
                String tgl = set.getString(2);
                String brt = set.getString(3);
                String sht = set.getString(4);
                String ccat = set.getString(5);
                String kalkulasi = set.getString(6);

                String[] data = {ID, tgl, brt, sht, ccat, kalkulasi};
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error tampil data" + e);
        }
    }

