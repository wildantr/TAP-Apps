    public void cari() {
        try {
            Statement stat = con.createStatement();
            String sql = "SELECT * FROM dataTelur Where id like '" + id.getText() + "'";
            ResultSet isi = stat.executeQuery(sql);
            if (isi.next()) {
                jLabel2.setText(isi.getString("tgl"));
                jTextField1.setText(isi.getString("brt"));
                jTextField5.setText(isi.getString("sht"));
                jTextField3.setText(isi.getString("cct"));
            } else {
                JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error:" + ex);
        }
    }
